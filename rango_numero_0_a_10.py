
# Actividad 02: Crear un programa, que lea un número y diga si está dentro del rango de 0 a 10, ambos inclusive

numero = int(input('Escribe un numero\r\n'))

if 0<= numero <=10:
    print("El número", numero,  "está dentro del rango de 0 a 10")
else:
    print("El número", numero, "está fuera del rango del 0 al 10")
